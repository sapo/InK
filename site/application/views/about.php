	<div class="ink-section" id="why">
		<div class="ink-vspace">
			<h3>Why use Ink</h3>
			<p>
				Ink presents a unique vision of what a front end framework should be. 
				More than a ready made solution (although it can be that), we went for a builing block approach. 
				By offering generic components that you can use in different contexts to obtain consistent results, Ink is both flexible and powerful while remaining easy to use.
			</p>
			<p>
				Our grid system is different from any other currently out there as it offers you true flexibility to build your layouts. 
				Because it's all percentage-based, the grid is truly fluid and you don't have to count columns and rely on memorizing offsets and spans. 
				Just naturally divide up your space in 10 to 100% blocks, divided into the most common steps, such as halves, quarters, fifths, etc.
			</p>
			<p>
				As to styling, we offer generic styling classes which can be applied to many elements in different contexts to obtain a consistent result. 
				Say you want a zebra-stripe table but also a zebra-stripe list. 
				With Ink, you use the same class .ink-zebra and the CSS takes care of applying correct layout and styling according to what type of markup element you used it on.</p>
			<p>
				This makes Ink very easy to learn and reuse over time, making your life easier. 
				And with a quick configurator, you can change the properties of any styiling class so that the default <code>.ink-warning</code> class is no longer red with white text, but becomes yellow with red text, for example. 
				You can then consistently use <code>.ink-warning</code> to get a red paragraph on a yellow background, or a yellow button with red text or even a yellow table row with red text in the table cells.
			</p>
		</div>
