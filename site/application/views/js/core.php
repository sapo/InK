<h2>SAPO</h2>

<p><em>require</em> - Load only the JavaScript that you need when you want.</p>

<p><em>browser</em> - Is the user on Internet Explorer? What version? What operating system? Browser detection should be used sparingly, but when you need it, we've got you covered.</p>

<p><em>namespace</em> - Sometimes it's convenient to extend the framework to fit your needs, maybe you want to add a new component, or maybe you want to create a new collection of classes. namespace creates an entry, based on the string passed into it, in the SAPO object that you can use to then add the properties and methods you want to create, if the namespace already exists, the object contained there is returned, otherwise it's a new object ready for you to extend.</p>

<p><em>bindObj and bindObjEvent</em> - Binding functions is an important concept of JavaScript but not all browsers support it, but don't worry, LibSAPO.js gives you the tools you need to get the job done. </p>

